# There are a number of variables listed below. 
# What are the different types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# `excited_dog` is a local variable because it is written in snake_case and has no symbols attached to it.
# `@excited_dog` is an instance variable because it begins with '@'
# `@@excited_dog` is a class variable because it starts with '@@'